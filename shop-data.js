// Fill `menuItems` to enable per-shop dropdown options on the order page.
export function getShopList(category) {
  return Object.values(SHOPS)
    .filter((shop) => shop.category === category)
    .map((shop) => shop.name);
}

export function findShopByName(name, category) {
  return Object.entries(SHOPS).find(([, shop]) => {
    const sameCategory = category ? shop.category === category : true;
    return sameCategory && shop.name === name;
  });
}

export function getShopsWithoutMenuItems() {
  return Object.values(SHOPS)
    .filter((shop) => !Array.isArray(shop.menuItems) || shop.menuItems.length === 0)
    .map((shop) => shop.name);
}
